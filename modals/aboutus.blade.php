           <p class="text-left w-75 m-auto">

           Lekki Concession Company Limited ("LCC") is a Special Purpose Vehicle set up specifically to execute the Eti-Osa Lekki Toll Road Concession Project. The project is designed to deliver essential road infrastructure and services along the Lekki Peninsular of Lagos.

Lekki Toll Road Concession was conceptualized as a Public Private Partnership (PPP) scheme, and uses the Build-Operate-Transfer (BOT) model of Infrastructure delivery. The Concession is for a period of 30 years, following which the assets will be transferred to Lagos State Government.

The project, given its pioneering nature, had some underlying assumptions and market indicators upon which the transaction was concluded and which have since drastically changed in a manner that it can no longer be sustained in its current form. Therefore, the State Government, after due consultation with all major stakeholders including the Lagos State House of Assembly entered into an Amicable Settlement Option with the shareholders of LCC with a view to bringing the project back on the path of long-term sustainability, acquired the shares/equity of the previous owners of LCC in December, 2014. This approach would particularly allow Government to take full control over the determination of toll rates in order to continue to make it affordable for road users.

On 1st June, 2016, Lekki Concession Company Limited was appointed by the Lagos State Government to take over the Operations & Maintenance of the Lekki-Ikoyi Link Bridge, due to the State's desire to restructure the operations of its assets on the Lekki-Epe Corridor under single umbrella.

LCC is strongly committed to providing high quality road infrastructure and related services to our customers and other stakeholders, and therefore places a premium on providing sustainable solutions to the challenges brought about by historic heavy traffic congestion along the Lekki Peninsular of Lagos. "We see more than a road".
